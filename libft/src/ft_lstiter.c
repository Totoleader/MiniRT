#include "../include/libft.h"

/*
Library :
	#include <libft.h>
Description :
	Iterates on the list 'lst' and applies the function 'f' to the content of
	each element.
Declaration :
	void ft_lstiter(t_list *lst, void (*f)(void *))
Parameters :
	lst - The address of the pointer to an element.
	del - The address of the function to be applied.
Return Value :
	None
*/
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst != NULL)
	{
		(*f)(lst->content);
		lst = lst->next;
	}
}
